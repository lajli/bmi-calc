# main.py
# Скрипт для расчета индекса массы тела (ИМТ)

# Ваши данные
your_name = "Мурзабаева Лайли"  # Впишите ваше имя
height_m = 1.75             # Рост в метрах (пример: 1.75)
weight_kg = 70              # Вес в килограммах (пример: 70)

# Расчет ИМТ по формуле: вес / (рост^2)
bmi = weight_kg / (height_m ** 2)

# Вывод результата
print("Расчет индекса массы тела (ИМТ)")
print(f"Выполнил: {your_name}")
print("-" * 30)
print(f"Рост: {height_m} м")
print(f"Вес: {weight_kg} кг")
print(f"ИМТ: {bmi:.2f}")  # Округление до двух знаков после запятой

# Простая интерпретация результата
if bmi < 18.5:
    print("Интерпретация: Недостаточный вес")
elif bmi < 25:
    print("Интерпретация: Нормальный вес")
elif bmi < 30:
    print("Интерпретация: Избыточный вес")
else:
    print("Интерпретация: Ожирение")